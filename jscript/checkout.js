	function mod10 (strNum) {

	   var nCheck = 0;

	   var nDigit = 0;

	   var bEven = false;

	   

	   for (n = strNum.length - 1; n >= 0; n--)

	   {

		  var cDigit = strNum.charAt (n);

		  if (isDigit (cDigit))

		  {

			 var nDigit = parseInt(cDigit, 10);

			 if (bEven)

			 {

				if ((nDigit *= 2) > 9)

				   nDigit -= 9;

			 }

			 nCheck += nDigit;

			 bEven = ! bEven;

		  }

		  else if (cDigit != ' ' && cDigit != '.' && cDigit != '-')

		  {

			 return false;

		  }

	   }

	   return (nCheck % 10) == 0;

	}

